#include "libft.h"

int	ft_free_tab(char **tab)
{
	int	i;

	i = 0;
	while (*(tab + i))
		i++;
	while (i >= 0)
	{
		free(*(tab + i));
		i--;
	}
	free(tab);
	return (0);
}
